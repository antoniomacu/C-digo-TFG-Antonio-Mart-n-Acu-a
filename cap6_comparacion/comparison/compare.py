# LATEX TABLE EXPORT
# ============================================================================

def export_latex_table(results: list[dict]):
    """Export comparison table as LaTeX booktabs snippet."""
    sorted_results = sorted(results, key=lambda r: r.get('auc_roc', 0), reverse=True)

    lines = []
    lines.append(r'\begin{table}[H]')
    lines.append(r'    \centering')
    lines.append(r'    \caption{Comparativa de arquitecturas de detección de anomalías. '
                 r'AUC-ROC, F1, Precisión y Recall calculados con umbral de Youden. '
                 r'MAE Ratio = MAE\_anómalo / MAE\_normal. '
                 r'Los modelos de producción (en negrita) utilizan los pesos finales obtenidos mediante '
                 r'optimización bayesiana con Optuna en hardware CUDA; '
                 r'sus métricas constituyen las referencias de rendimiento del sistema desplegado.}')
    lines.append(r'    \label{tab:benchmark-comparison}')
    lines.append(r'    \begin{tabular}{lcccccc}')
    lines.append(r'    \toprule')
    lines.append(r'    Modelo & Tipo & AUC-ROC & F1 & Precisión & Recall & MAE Ratio \\')
    lines.append(r'    \midrule')

    for r in sorted_results:
        name = r['model_name']
        m_type = 'Red neuronal' if r['model_type'] == 'neural_network' else 'ML clásico'
        auc  = f"{r.get('auc_roc', 0):.4f}"
        f1   = f"{r.get('f1', 0):.4f}"
        prec = f"{r.get('precision', 0):.4f}"
        rec  = f"{r.get('recall', 0):.4f}"
        mae_r = f"{r['mae_ratio']:.4f}" if r.get('mae_ratio') is not None else '--'

        # Bold both production model rows
        _prod = {'VAE (Producción)', 'CVAE Nivel 1 (Producción)'}
        if name in _prod:
            row = (f'    \\textbf{{{name}}} & \\textbf{{{m_type}}} & '
                   f'\\textbf{{{auc}}} & \\textbf{{{f1}}} & '
                   f'\\textbf{{{prec}}} & \\textbf{{{rec}}} & '
                   f'\\textbf{{{mae_r}}} \\\\')
        else:
            row = f'    {name} & {m_type} & {auc} & {f1} & {prec} & {rec} & {mae_r} \\\\'
        lines.append(row)

    lines.append(r'    \bottomrule')
    lines.append(r'    \end{tabular}')
    lines.append(r'\end{table}')

    tex_content = '\n'.join(lines) + '\n'
    save_path = RESULTS_DIR / 'comparison_table.tex'
    save_path.write_text(tex_content, encoding='utf-8')
    print(f"  LaTeX table saved to {save_path}")


# ============================================================================
# ENTRY POINT
# ============================================================================

def main():
    import matplotlib
    matplotlib.rcParams.update({'font.size': 11})

    results = load_results()
    print(f"Loaded {len(results)} model results\n")

    # Generate all plots
    print("Generating comparison charts...")
    plot_metrics_comparison(results)
    plot_roc_curves(results)
    plot_precision_recall(results)
    plot_mae_comparison(results)

    # Console ranking
    print_full_ranking(results)

    export_latex_table(results)

    print(f"\nAll charts saved to {RESULTS_DIR}/")


if __name__ == '__main__':
    main()
